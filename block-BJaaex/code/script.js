let rootElm = document.querySelector(".cardHolder");
let reset = document.querySelector("h2");
let count = document.querySelector(".count");
let timer = document.querySelector(".timer");

let match = [];
let matchedCards = [];
let firstClick = [];

let countStep = 0;
let time ;

count.innerText = `Count : 0`;
timer.innerText = 0;

cardData.sort(() => 0.5 - Math.random());

function hideValue() {
    cardData.forEach((card) => {card.display = false});
    match = [];
    renderCards();
    getMatch();
    renderCards();
}

function getMatch() {

    if (match.length === 2) {

        if(match[0].name === match[1].name) {      
            matchedCards.push(match[0],match[1]);
            match = [];
            getResult();
            getMatch();
            countStep = Number(countStep) + 1; 
            count.innerText = `Count : ${countStep}`; 
        } else{
            matchedCards = [];
            match = [];
            setTimeout(hideValue, 500);
            renderCards();
            countStep = Number(countStep) + 1;
            count.innerText = `Count : ${countStep}`; 
        }
    }
}

function handleShuffle() {
    cardData.sort(() => 0.5 - Math.random());
    cardData.forEach((val) => {
        val.display = false;
    })
    match = [];
    count.innerText = `Count : 0`; 
    firstClick = [];
    timer.innerText = 0;
    renderCards();
    getMatch();
    clearInterval(time);
}

reset.addEventListener("click", handleShuffle)

function getResult() {
    if(matchedCards.length === cardData.length) {
        setTimeout( ()=> {
            alert(`You won! Time required to win is ${timer.innerText}`);
            timer.innerText = 0;
            handleShuffle();
            getFirstClick();
        },1000)
     }
}

function getTimer() {
    timer.innerText = Number(timer.innerText) + 1;
}

function getFirstClick() {
    if(firstClick.length === 1) {
         time = setInterval(getTimer, 1000);
    } else if(matchedCards.length === cardData.length){
        clearInterval(time);      
    }
}

function renderCards() {

    rootElm.innerHTML = "";

    cardData.forEach((card, index)=> {

        let div = document.createElement("div");
        div.classList.add("card");
        div.setAttribute("data-name", card.name);
        div.setAttribute("data-isDisplay", card.display);
        div.setAttribute("data-path", card.path);
        div.setAttribute("data-id", index);
        
        div.addEventListener("click", (event)=> {
            let val = event.target.dataset;
            let id = val.id;
            cardData[id].display = true;
            match.push(val);

            renderCards();
            getMatch();

            firstClick.push(id);
            getFirstClick();
        })

        if(card.display === false) {
            div.style.backgroundColor = "bisque";
        } else{
            div.innerHTML = card.path;
        }
        rootElm.append(div);
    })
}
renderCards();
